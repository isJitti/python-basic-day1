#ข้อ2 รับคะแนนสอบ → แสดงผลว่า "คุณสอบผ่าน" หรือ "คุณสอบไม่ผ่าน" (ใช้ if / else)
score = int(input("บอกเกรดของคุณ : "))   #ต้องแปลง input เป็น “เลข” ก่อนด้วย int()
if score >= 95 :
    print("ได้ A ดีเยี่ยมผ่าน!!!")
elif score >= 80 :
    print("ได้ B ดีผ่าน")
elif score >= 70 :
    print("ได้ C ปรับปรุง")
elif score >= 60 :
    print("ได้ D ปรับปรุงแก้ไข")
elif score >= 50 :
    print("ได้ D ปรับปรุงด่วนๆ!!!")
else :
    print("ได้ F ไม่ผ่าน !!!")
