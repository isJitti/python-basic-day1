#ข้อ 5: โปรแกรมบอกฤดูจากตัวเลขเดือน รับเดือน 1–12 (เป็นตัวเลข) แล้วบอกว่าเป็นฤดูไหน
#มีนาคม–พฤษภาคม = ฤดูร้อน มิถุนายน–ตุลาคม = ฤดูฝน พฤศจิกายน–กุมภาพันธ์ = ฤดูหนาว ใช้: int(input()) if / elif / else
month = int(input("บอกเลขเดือนแล้วจะบอกฤดูให้ : "))
if 3 <= month <= 5:
    print("ฤดูร้อนจร้า")
elif 6 <= month <= 10:
    print("ฤดูฝนจร้า")
elif 1 <= month <= 12:
    print("ฤดูหนาวจร้า")
else:
    print("เดือนผิดนะ 1-12 เท่านั้น")
