#ข้อ 4 โปรแกรมเทียบตัวเลข 2 ตัว (ใหญ่กว่า / น้อยกว่า / เท่ากัน)
#โจทย์:รับเลข 2 ตัวจากผู้ใช้ → บอกว่าเลขไหนใหญ่กว่าหรือถ้าเท่ากัน ให้บอกว่า "เท่ากัน"
num1 = int(input("บอกตัวเลขที่ 1 : "))
num2 = int(input("บอกตัวเลขที่ 2 : "))
if num1 > num2 :
    print("อันนี้ใหญ่กว่า : " , num1 )
elif num1 < num2 :
    print("อันนี้น้อยกว่า : " , num1 )
else :
    print("อันนี้เท่ากัน : " , num1 , num2 )
